        <p class="content_text">Музей Сценического Искусства им. В. Э. Мейерхольда («Дом Мейерхольда») открыт в Пензе 24 февраля 1984 года к 110-летию со дня рождения режиссера-реформатора. Расположен музей в мемориальном доме – бывшей городской усадьбе семьи купца второй гильдии Э.Ф.Мейергольда на улице Володарского (бывшей Лекарской) в доме № 59.

            Деревянное здание, имеющее цокольный этаж, общей площадью 340 кв.м, реставрировано в 1979-82 годах с полным сохранением мемориальной планировки и дизайна фасада. Первая экспозиция музея была построена как обзорная, рассказывающая о жизни и творчестве деятелей отечественного искусства, уроженцев Пензенского края, в том числе о В.И.Пудовкине, Л.А.Руслановой, А.С.Архангельском, братьях Мозжухиных.

            Новая экспозиция (1994) полностью посвящена жизни и творчеству В.Э.Мейерхольда. Бытовая часть экспозиции рассказывает о жизни семьи Мейерхольда в Пензе, о его гимназических годах, окружении, первых шагах на сценическом поприще. Музей располагает интересными мемориальными материалами, связанными с В.Э.Мейерхольдом и его окружением. На базе музея в 1989 и 1994 годах проходили международные научно-практические конференции, приуроченные к юбилейным датам. Они были посвящены новейшим исследованиям творческого наследия великого режиссера. С 1989 года при музее создан государственный экспериментальный театр-студия «Авансцена».

            20 ноября 1999 года на территории музея открыт памятник В.Э.Мейерхольду (бронза, скульптор Ю.Е.Ткаченко).</p>
