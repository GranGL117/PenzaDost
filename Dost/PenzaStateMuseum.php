        <h1 class="content_title">Пензенский государственный краеведческий музей</h1>
        <p class="content_text">Музей основан 22 сентября 1905 года Пензенским обществом любителей естествознания (ПОЛЕ). В 1924 г. музей преобразован в краеведческий. Инициаторами исторической ориентации музея были Нонна Ивановна Спрыгина, положившая начало этнографическим и археологическим исследованиям, и Борис Николаевич Гвоздев, организовавший в 1921 г. исторический отдел и развернувший его активную выставочную деятельность.
            Собрание музея включает естественнонаучные, археологические, этнографические, художественные, нумизматические коллекции, фонды письменных и печатных источников, а его экспозиции, представляющие природу и историю края, интересны и взрослым, и детям.

            Экспозиция "Геологическое и палеонтологическое прошлое Пензенского края" познакомит посетителей с образцами фауны ледниковой эпохи - скелетами мамонта, бизона, шерстистого носорога. Экспозиция "Природа Пензенского края и её охрана" покажет многообразие животного и растительного мира на территории области. История основания крепости Пенза и дальнейшее развитие города до 1917 года отражены в экспозиции "Старинный город с русскою душою". Всем интересующимся военной историей будут интересны выставки "Поле ратной славы. Пензенцы в Отечественной войне 1812 года", "Немеркнущая слава подвига" и "Революции и Пензенский край. 1917-1939 гг." - экспонаты этих выставок помогут погрузиться в атмосферу тех событий и разобраться в сложных вопросах отечественной истории.

            В настоящее время в состав Пензенского государственного краеведческого музея входят на правах отделов музей В.О. Ключевского, музей И.Н. Ульянова, дом-музей Н.Н. Бурденко, музей народного творчества, музей Золотаревского городища</p>
